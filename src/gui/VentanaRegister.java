    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String nombre = jTextField1.getText();
        String contraseña1 = new String(jPasswordField1.getPassword());
        String contraseña2 = new String(jPasswordField2.getPassword());
        int opcion = JOptionPane.showConfirmDialog(this, "¿Eres administrador?");
        boolean administrador = false;
        
        if(opcion==0){
            administrador=true;
        }
        
        if (contraseña1.equals(contraseña2)) {
            if (Acceso.getId(nombre, contraseña2) == -1) {
                if (Acceso.addUsuarios(nombre, contraseña2,administrador)) {
                    try {
                        conexionUsuarios.guardarDatos(nombre + ";" + contraseña1 + ";" + administrador);
                    } catch (IOException ex) {
                    }
                    JOptionPane.showMessageDialog(null, "se ha registrado exitosamente");
                    
                    if (Acceso.getId(nombre, contraseña2) != -1) {
                        JOptionPane.showMessageDialog(null, "Su id es: " + (Acceso.getId(nombre, contraseña2)));
                    }
                    jTextField1.setText("");
                    jPasswordField1.setText("");
                    jPasswordField2.setText("");
                } else {
                    JOptionPane.showMessageDialog(null, "Ocurrio un error en el registro");
                }
            }else{
                JOptionPane.showMessageDialog(this, "El usuario ya esta registrado");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Las contraseñas no son iguales");
        }

    }//GEN-LAST:event_jButton1ActionPerformed

