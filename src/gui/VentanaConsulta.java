    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            String titulo = jTextField1.getText();
            String autor = jTextField3.getText();
            int añoDePublicacion = Integer.parseInt(jTextField4.getText());
            String genero = jTextField2.getText();

            if (Acceso.consultarLibro(titulo.toLowerCase(), autor.toLowerCase(), añoDePublicacion, genero.toLowerCase())) {
                JOptionPane.showMessageDialog(null, "Libro si esta en la biblioteca");
            } else {
                JOptionPane.showMessageDialog(null, "Libro no se encuentra en biblioteca");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Ah ocurrido un error");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

