    public void cargarUsuarios() throws IOException {
        if (!conexionUsuarios.leerDatos().equals("")) {
            String[] lineas = conexionUsuarios.leerDatos().split("\n");
            for (String linea : lineas) {
                String[] datos = linea.split(";");
                Acceso.addUsuarios(datos[0],datos[1],Boolean.parseBoolean(datos[2]));
            }
        }
    }
    
