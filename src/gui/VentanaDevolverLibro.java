    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int id = Integer.parseInt(txtId.getText());
        String titulo = txtTitulo.getText();
        String autor = txtAutor.getText();
        int añoDePublicacion = Integer.parseInt(txtAño.getText());
        String genero = txtGenero.getText();

        if(Acceso.devolverLibro(id,titulo,autor,añoDePublicacion,genero)){
            Acceso.addLibro(titulo, autor, añoDePublicacion, genero);
            try {
                conexion.guardarDatos(titulo+";"+autor+";"+añoDePublicacion+";"+genero);
            } catch (IOException ex) {
            }
            JOptionPane.showMessageDialog(this, "Gracias por devolver el libro");
            try {
                conexionLibrosPrestado.removeDatos(titulo);
            } catch (IOException ex) {
            }
            txtId.setText("");
            txtTitulo.setText("");
            txtAutor.setText("");
            txtAño.setText("");
            txtGenero.setText("");
        }else{
            JOptionPane.showMessageDialog(this, "Ah ocurrido un error");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

